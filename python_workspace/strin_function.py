str_1 = "         문자열 함수 알아보기 Hello Python  "

print(" lower  : 소문자로 변경 - >", str_1.lower())
print(" upper  : 대문자로 변경 - >", str_1.upper())
print(" strip  : 양쪽 공백문자 제거 - >", str_1.strip())
print(" rstrip  : 오른쪽 공백문자 제거 - >", str_1.rstrip())
print(" lstrip  : 왼쪽 공백문자 제거 - >", str_1.lstrip())

